import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { normalizeLineEndings } from "./line-endings.mjs";

const root = process.cwd();
const blogDirectory = path.join(root, "content", "blog");
const outputPath = path.join(root, "src", "data", "blog.generated.ts");

function parseScalar(value) {
  const trimmed = value.trim();
  if (trimmed === "true") return true;
  if (trimmed === "false") return false;
  if (trimmed === "null") return null;
  if (trimmed.startsWith('"') && trimmed.endsWith('"')) {
    return JSON.parse(trimmed);
  }
  if (trimmed.startsWith("'") && trimmed.endsWith("'")) {
    return trimmed.slice(1, -1).replace(/''/g, "'");
  }
  return trimmed;
}

function parseIndexYaml(source, slug) {
  const entry = {};
  let arrayKey = null;

  for (const rawLine of source.split(/\r?\n/)) {
    if (!rawLine.trim() || rawLine.trimStart().startsWith("#")) continue;

    const arrayItem = rawLine.match(/^\s+-\s+(.*)$/);
    if (arrayItem && arrayKey) {
      entry[arrayKey].push(parseScalar(arrayItem[1]));
      continue;
    }

    const field = rawLine.match(/^([A-Za-z][A-Za-z0-9]*):(?:\s*(.*))?$/);
    if (!field) throw new Error(`Unsupported blog metadata in ${slug}: ${rawLine}`);

    const [, key, value = ""] = field;
    if (!value.trim()) {
      entry[key] = [];
      arrayKey = key;
    } else {
      entry[key] = parseScalar(value);
      arrayKey = null;
    }
  }

  return entry;
}

const directoryEntries = await readdir(blogDirectory, { withFileTypes: true });
const records = [];

for (const directoryEntry of directoryEntries) {
  if (!directoryEntry.isDirectory()) continue;

  const slug = directoryEntry.name;
  const articleDirectory = path.join(blogDirectory, slug);
  const [indexSource, bodySource] = await Promise.all([
    readFile(path.join(articleDirectory, "index.yaml"), "utf8"),
    readFile(path.join(articleDirectory, "body.mdoc"), "utf8").then(normalizeLineEndings),
  ]);

  records.push({
    slug,
    entry: {
      ...parseIndexYaml(indexSource, slug),
      bodySource,
    },
  });
}

records.sort((first, second) => first.slug.localeCompare(second.slug));

const output = `// This file is generated by scripts/generate-blog-manifest.mjs.
// Edit content/blog source files, then rerun the manifest generator.

export type BlogPostEntry = {
  readonly title: string;
  readonly excerpt: string;
  readonly publishedDate: string;
  readonly updatedDate: string;
  readonly author: string;
  readonly cover: string | null;
  readonly coverAlt: string;
  readonly tags: readonly string[];
  readonly featured: boolean;
  readonly draft: boolean;
  readonly seoTitle: string;
  readonly seoDescription: string;
  readonly bodySource: string;
};

export type BlogPostRecord = {
  readonly slug: string;
  readonly entry: BlogPostEntry;
};

export const blogPosts = ${JSON.stringify(records, null, 2)} as const satisfies readonly BlogPostRecord[];
`;

await writeFile(outputPath, output, "utf8");
console.log(`Blog manifest is current (${records.length} posts).`);
