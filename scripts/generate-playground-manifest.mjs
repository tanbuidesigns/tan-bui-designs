import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const repositoryRoot = path.resolve(scriptDirectory, "..");
const assetDirectory = path.join(repositoryRoot, "public", "playground");
const outputFile = path.join(repositoryRoot, "src", "data", "playground.generated.ts");
const layouts = ["wide", "tall", "square", "standard", "panorama", "portrait"];
const corners = ["soft", "swept", "opposite", "round"];

await mkdir(assetDirectory, { recursive: true });

const entries = await readdir(assetDirectory, { withFileTypes: true });
const filenames = entries
  .filter((entry) => entry.isFile() && /\.webp$/i.test(entry.name))
  .map((entry) => entry.name)
  .sort((first, second) => first.localeCompare(second, "en", { numeric: true, sensitivity: "base" }));

const titleCase = (value) =>
  value
    .replace(/[-_]+/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .split(" ")
    .map((word) => {
      const lower = word.toLowerCase();
      if (lower === "3d") return "3D";
      if (["ui", "ux", "pos"].includes(lower)) return lower.toUpperCase();
      return lower.charAt(0).toUpperCase() + lower.slice(1);
    })
    .join(" ");

const stableHash = (value) => {
  let hash = 2166136261;
  for (const character of value) {
    hash ^= character.charCodeAt(0);
    hash = Math.imul(hash, 16777619);
  }
  return hash >>> 0;
};

const items = filenames.map((filename) => {
  const stem = path.basename(filename, path.extname(filename));
  const separatorIndex = stem.indexOf("__");
  const categorySource = separatorIndex >= 0 ? stem.slice(0, separatorIndex) : "Design exploration";
  const titleSource = separatorIndex >= 0 ? stem.slice(separatorIndex + 2) : stem;
  const category = titleCase(categorySource) || "Design exploration";
  const title = titleCase(titleSource) || "Untitled exploration";
  const id = stem
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
  const hash = stableHash(stem);

  if (!id) throw new Error(`Could not derive a stable identifier from ${filename}`);

  return {
    id,
    kind: "image",
    src: `/playground/${filename}`,
    category,
    title,
    alt: `${title} — ${category}`,
    layout: layouts[hash % layouts.length],
    corner: corners[Math.floor(hash / layouts.length) % corners.length],
    art: hash % 6,
  };
});

const output = `// This file is generated by scripts/generate-playground-manifest.mjs.
// Add WebP files to public/playground and rerun the manifest generator.

export type PlaygroundLayout = "wide" | "tall" | "square" | "standard" | "panorama" | "portrait";
export type PlaygroundCorner = "soft" | "swept" | "opposite" | "round";

export type PlaygroundItem = {
  readonly id: string;
  readonly kind: "image" | "placeholder";
  readonly src?: string;
  readonly category: string;
  readonly title: string;
  readonly alt: string;
  readonly layout: PlaygroundLayout;
  readonly corner: PlaygroundCorner;
  readonly art: number;
};

export const playgroundItems = ${JSON.stringify(items, null, 2)} as const satisfies readonly PlaygroundItem[];
`;

let currentOutput = "";
try {
  currentOutput = await readFile(outputFile, "utf8");
} catch (error) {
  if (error.code !== "ENOENT") throw error;
}

if (currentOutput !== output) {
  await writeFile(outputFile, output, "utf8");
  console.log(`Generated ${path.relative(repositoryRoot, outputFile)} with ${items.length} Playground items.`);
} else {
  console.log(`Playground manifest is current (${items.length} items).`);
}
