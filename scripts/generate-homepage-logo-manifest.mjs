import { mkdir, readFile, readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDirectory = path.dirname(fileURLToPath(import.meta.url));
const repositoryRoot = path.resolve(scriptDirectory, "..");
const logoDirectory = path.join(repositoryRoot, "public", "homepage");
const outputFile = path.join(
  repositoryRoot,
  "src",
  "data",
  "homepageLogos.generated.ts"
);

const entries = await readdir(logoDirectory, { withFileTypes: true });
const filenames = entries
  .filter((entry) => entry.isFile() && /\.webp$/i.test(entry.name))
  .map((entry) => entry.name)
  .sort((first, second) =>
    first.localeCompare(second, "en", {
      numeric: true,
      sensitivity: "base",
    })
  );

if (filenames.length === 0) {
  throw new Error(`No WebP logos found in ${logoDirectory}`);
}

const usedIdentifiers = new Set();
const logos = filenames.map((filename) => {
  const extension = path.extname(filename);
  const readableName = path
    .basename(filename, extension)
    .replace(/[_-]+/g, " ")
    .replace(/\s+/g, " ")
    .trim();
  const id = readableName
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

  if (!id) {
    throw new Error(`Could not derive a stable identifier from ${filename}`);
  }

  if (usedIdentifiers.has(id)) {
    throw new Error(`Duplicate homepage logo identifier: ${id}`);
  }

  usedIdentifiers.add(id);

  return {
    id,
    src: `/homepage/${filename}`,
    alt: `${readableName} logo`,
  };
});

const output = `// This file is generated by scripts/generate-homepage-logo-manifest.mjs.
// Add WebP files to public/homepage and rerun the generator.

export type HomepageLogo = {
  readonly id: string;
  readonly src: string;
  readonly alt: string;
};

export const homepageLogos = ${JSON.stringify(logos, null, 2)} as const satisfies readonly HomepageLogo[];
`;

await mkdir(path.dirname(outputFile), { recursive: true });

let currentOutput = "";

try {
  currentOutput = await readFile(outputFile, "utf8");
} catch (error) {
  if (error.code !== "ENOENT") {
    throw error;
  }
}

if (currentOutput !== output) {
  await writeFile(outputFile, output, "utf8");
  console.log(`Generated ${path.relative(repositoryRoot, outputFile)} with ${logos.length} logos.`);
} else {
  console.log(`Homepage logo manifest is current (${logos.length} logos).`);
}
